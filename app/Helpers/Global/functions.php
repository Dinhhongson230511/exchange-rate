function dateTime($pushTime = 0)
{
    return date('Y-m-d H:i:s', time() + $pushTime);
}

function getDay($pushDate = 0)
{
    $day = date('Y-m-d');

    if($pushDate == 0) return $day;

    $pushDay = $pushDate * 24 * 60 * 60;

    $time = strtotime($day) + $pushDay;

    return date('Y-m-d', $time);
}